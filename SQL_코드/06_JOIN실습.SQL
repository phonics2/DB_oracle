-- 1. EMPLOYEE 테이블과 DEPARTMENT 테이블을 조인해서
-- 각 직원의 이름과 그 직원이 속한 부서명을 조회
--ANSI
SELECT EMP_NAME AS "직원 이름" , DEPT_TITLE AS "부서명"
FROM EMPLOYEE
JOIN DEPARTMENT ON (DEPT_ID = DEPT_CODE);
--ORACLE


-- 2. EMPLOYEE 테이블과 DEPARTMENT 테이블을 조인해서
-- 각 직원의 이름과 그 직원이 속한 부서명, 급여 조회
--ANSI
SELECT EMP_NAME AS "직원이름", DEPT_TITLE AS "부서명" , SALARY AS "급여"
FROM EMPLOYEE
JOIN DEPARTMENT ON (DEPT_ID = DEPT_CODE);

-- ORACLE
SELECT EMP_NAME, DEPT_TITLE, SALARY
FROM EMPLOYEE, DEPARTMENT
WHERE DEPT_CODE = DEPT_ID;

-- 3. EMPLOYEE 테이블과 SAL_GRADE 테이블 조인해서
-- 각 직원의 이름과 급여 등급 조회
--ANSI
SELECT EMPLOYEE.EMP_NAME, SAL_GRADE.SAL_LEVEL
FROM EMPLOYEE
JOIN SAL_GRADE ON (EMPLOYEE.SAL_LEVEL = SAL_GRADE.SAL_LEVEL);
/*
다른 테이블에서 컬럼명은 다르지만 서로 의미하는 바가 같은 컬럼끼리는
컬럼명1 = 컬럼명A 사용할 수 있음

다른 테이블에서 컬럼명이 같을 때는 참조하는 테이블이 무엇인지 작성해줘야함
테이블명.컬럼명1 = 테이블명.컬럼명A
SELECT 로 참조할 때도 테이블명 작성해서 어디서 어느컬럼을 가지고오는지 작성
*/

--ORACLE
SELECT EMPLOYEE.EMP_NAME AS "직원이름", SAL_GRADE.SAL_LEVEL AS "급여수준"
FROM EMPLOYEE , SAL_GRADE
WHERE EMPLOYEE.SAL_LEVEL = SAL_GRADE.SAL_LEVEL;

